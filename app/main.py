@app.delete(
    "/documents/{doc_id}",
    response_model=Dict[str, Any],
    status_code=status.HTTP_200_OK,
    tags=["Ingestion"],
    summary="Delete an indexed document",
    description="Purges document metadata from SQLite, chunks from ChromaDB, and raw files from uploads disk."
)
async def remove_document(doc_id: str):
    docs = get_all_documents()
    target_doc = None
    for doc in docs:
        if doc["id"] == doc_id:
            target_doc = doc
            break
            
    if not target_doc:
        raise HTTPException(
            status_code=status.HTTP_404_NOT_FOUND, 
            detail="Document not found."
        )
        
    # Delete from ChromaDB
    try:
        get_chroma_manager().delete_document_chunks(doc_id)
    except Exception as e:
        logger.error(f"Failed to purge ChromaDB vectors for doc ID {doc_id}: {e}")
        
    # Delete raw file
    filepath = Path(target_doc["filepath"])
    if filepath.exists():
        try:
            filepath.unlink()
            logger.info(f"Purged uploaded file from disk: {filepath.name}")
        except Exception as e:
            logger.error(f"Failed to delete upload file {filepath}: {e}")
            
    # Delete from SQLite
    try:
        delete_document(doc_id)
    except Exception as e:
        logger.error(f"Failed to remove SQLite document metadata for doc ID {doc_id}: {e}")
        raise HTTPException(
            status_code=status.HTTP_500_INTERNAL_SERVER_ERROR,
            detail="Failed to complete document deletion from metadata database."
        )
        
    return {
        "status": "success", 
        "detail": f"Document {target_doc['filename']} has been permanently deleted."
    }

@app.get(
    "/health", 
    response_model=SystemHealthResponse,
    status_code=status.HTTP_200_OK,
    tags=["Monitoring"],
    summary="Get API health status",
    description="Returns detailed performance stats, active file counts, and provider availability."
)
async def health_check():
    llm_ok = is_llm_available()
    docs = get_all_documents()
    
    total_docs = len(docs)
    total_chunks = sum(doc.get("chunk_count", 0) for doc in docs)
    
    # Calculate sizes
    sqlite_size = 0
    if settings.SQLITE_DB_PATH.exists():
        sqlite_size = settings.SQLITE_DB_PATH.stat().st_size
        
    chroma_count = 0
    try:
        chroma_count = get_chroma_manager().collection.count()
    except Exception:
        pass
        
    return {
        "status": "healthy",
        "llm_available": llm_ok,
        "uptime_seconds": time.time() - START_TIME,
        "metrics": {
            "document_count": total_docs,
            "chunk_count": total_chunks,
            "chroma_vector_count": chroma_count,
            "sqlite_db_size_bytes": sqlite_size,
            "settings": {
                "llm_provider": settings.LLM_PROVIDER.upper(),
                "llm_model": settings.LLM_MODEL,
                "rate_limit_limit": settings.RATE_LIMIT_PER_MINUTE
            }
        }
    }
